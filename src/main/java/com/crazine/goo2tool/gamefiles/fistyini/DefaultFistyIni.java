            "; \n" +
            "; Generated by Goo2Tool\n\n";
    
    public static FistyIniFile generateBallTable() {
        StringBuilder builder = new StringBuilder(BALL_TABLE_HEADER);
        
        for (int i = 0; i < BALL_TABLE.length; i++) {
            builder.append(i).append('=').append(BALL_TABLE[i]).append('\n');
        }
        
        return new FistyIniFile(builder.toString(), List.of(BALL_TABLE));
    }
    
